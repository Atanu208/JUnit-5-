public class MathUtils {

    public int add(int a,int b)
    {
        return a+b;
    }

    public double computeCircleArea(int radious)
    {
        return 3.14 * radious * radious;
    }

    public double devide(int numerator,int denominator)
    {
        return numerator/denominator;
    }
    int sub(int numerator,int denominator)
    {
        return numerator-denominator;
    }
}
